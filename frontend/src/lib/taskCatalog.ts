export function sectionOrder(section: string): number {
  const index = LEARNING_SECTION_ORDER.indexOf(section as (typeof LEARNING_SECTION_ORDER)[number]);
  return index >= 0 ? index : 100;
}

export function sectionDescription(section: string): string {
  return SECTION_DESCRIPTIONS[section] ?? 'учебные задачи этого раздела';
}

export function difficultyOrder(difficulty: string): number {
  const order: Record<string, number> = {
    easy: 1,
    medium: 2,
    hard: 3,
  };
  return order[difficulty] ?? 50;
}

export function difficultyLabel(difficulty: string | null): string {
  const labels: Record<string, string> = {
    easy: 'легкая',
    medium: 'средняя',
    hard: 'сложная',
  };
  return labels[difficulty ?? ''] ?? difficulty ?? '';
}
